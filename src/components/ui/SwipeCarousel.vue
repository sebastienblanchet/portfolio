<template>
  <div>
    <v-carousel v-model="model" hide-delimiters>
      <v-carousel-item
        v-touch="{
        left: () => prevTab(),
        right: () => nextTab(),
        }"
        v-for="(color, i) in colors"
        :key="color"
      >
        <v-sheet
          :color="color"
          height="100%"
          tile
        >
          <v-row
            class="fill-height"
            align="center"
            justify="center"
          >
            <div class="display-3">Slide {{ i + 1 }} {{model}}</div>
          </v-row>
        </v-sheet>
      </v-carousel-item>
    </v-carousel>
  </div>
</template>

<script>
  export default {
    data () {
      return {
        colors: [
          'primary',
          'secondary',
          'yellow darken-2',
          'red',
          'orange',
        ],
        model: 0,
      }
    },
    methods: {
      prevTab() {
        console.log("Swipe left");
        this.model = (this.model == 0 ) ? this.colors.length : this.model--;
      },
      nextTab() {
        console.log("Swipe right");
        this.model = (this.model == this.colors.length ) ? 0 : this.model++;
      }
    }
  }
</script>